-- 建表（若已执行过可跳过）
CREATE TABLE IF NOT EXISTS `dm` (
    `id`           BIGINT       NOT NULL AUTO_INCREMENT COMMENT 'DM ID',
    `store_id`     BIGINT       NOT NULL                COMMENT '所属门店 ID（关联 store.id）',
    `name`         VARCHAR(50)  NOT NULL                COMMENT 'DM 姓名 / 艺名',
    `avatar`       VARCHAR(255) DEFAULT NULL            COMMENT '头像 URL',
    `introduction` TEXT         DEFAULT NULL            COMMENT 'DM 个人简介',
    `style_tags`   VARCHAR(255) DEFAULT NULL            COMMENT '风格标签，英文逗号分隔',
    `rating`       DECIMAL(2,1) DEFAULT 0.0             COMMENT '综合评分（0.0-5.0）',
    `game_count`   INT          DEFAULT 0               COMMENT '累计主持场次',
    `status`       TINYINT      DEFAULT 1               COMMENT '状态：1在职 0离职',
    `is_deleted`   TINYINT      NOT NULL DEFAULT 0      COMMENT '逻辑删除：0未删除 1已删除',
    `create_time`  DATETIME     DEFAULT CURRENT_TIMESTAMP          COMMENT '创建时间',
    `update_time`  DATETIME     DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    INDEX `idx_store_id` (`store_id`),
    INDEX `idx_status`   (`status`),
    INDEX `idx_deleted`  (`is_deleted`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='DM（主持人）表';


-- 排期表新增 DM 字段
-- 注意：如果已执行过请跳过此句，否则会报 Duplicate column 错误
ALTER TABLE `script_schedule`
    ADD COLUMN `dm_id` BIGINT DEFAULT NULL
    COMMENT '主持 DM 的 ID，可为空（关联 dm.id）'
    AFTER `room_id`;

-- 评价表新增 DM 评分字段
-- 注意：如果已执行过请跳过此句
ALTER TABLE `review`
    ADD COLUMN `dm_id`     BIGINT  DEFAULT NULL COMMENT '本场 DM ID（关联 dm.id）'  AFTER `service_rating`,
    ADD COLUMN `dm_rating` TINYINT DEFAULT NULL COMMENT 'DM 评分（1-5星）'           AFTER `dm_id`;

-- 预约表新增 DM 字段
-- 注意：如果已执行过请跳过此句
ALTER TABLE `reservation`
    ADD COLUMN `dm_id` BIGINT DEFAULT NULL
    COMMENT '预约时选择的 DM ID（关联 dm.id）'
    AFTER `schedule_id`;


